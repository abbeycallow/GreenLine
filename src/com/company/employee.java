package com.company;

abstract class employee {
    String name;
    int employeeId;

    public void setName(String newName) {
        name = newName; }

    public String getName() {
        return name; }

    public void setEmployeeId(int newEmployeeId) {
        employeeId = newEmployeeId;}

    public int getEmployeeId() { return employeeId; }


}













